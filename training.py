    p2_potential_VP = which_three_in_a_line(p2_potential)
    p2_potential_twos = which_three_in_a_line(p2_potential, TWO_IN_A_ROW_MASK)

    # Flatten the last three dimensions into a single vector using einops
    flat_one_hot = rearrange(one_hot, "... N_ROWS N_COLS C -> ... (N_ROWS N_COLS C)")

    # Create input vector x
    x = jnp.concatenate([flat_one_hot, p1_potential_VP, p1_potential_twos, p2_potential_VP, p2_potential_twos], axis=-1)

    first = jnp.dot(x, W1) + b1

def loss(params,board, actual, which_ones):
  vals = value(params, board);
  squared_error = (vals - actual)**2
  masked_losses = squared_error * which_ones
  return jnp.sum(masked_losses) / jnp.sum(which_ones)

def action_loss(params, board, actual, actions, which_ones):
  q_vals = roll_again_value(params, board)

  # create a one hot mask that selects one of the actions [stop, roll_again]
  mask = jax.nn.one_hot(actions, num_classes=2)

  # calculate the loss by selecting the result of q vals and comparing it to the actual value
  chosen_q_val = jnp.sum(q_vals * mask, axis=-1)
  squared_error = (chosen_q_val - actual)**2
  masked_losses = squared_error * which_ones
  return jnp.sum(masked_losses) / jnp.sum(which_ones)

